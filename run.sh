#!/bin/bash

# Compile the Java source code
make

# Run the Java application
make run

####### To run the application, copy the following command into the terminal
#######        chmod +x run.sh
####### This command will make the script executable for the user (authorizing the user to run the script)
####### In macOS, the user can run the script by dragging the file into the terminal and pressing enter under the directory of the file